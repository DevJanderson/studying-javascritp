// statements: Uma statements é uma instrução de código que executa uma ação

// definir uma váriavel é uns statements(instrução)
let text = "Hello world!"

// chamar um função é uma statements(instru)
console.log(text)

// Instruções pode ser separados por ponto e virgula.
console.log("Um"); console.log("Dois"); console.log("Três")

// Instruções pode ser separados por quebra de linhas
console.log("Um")
console.log("Dois")
console.log("Três")

// Instruções pode ser separadas por quebras de linhas e ponto e virgula.
console.log("Um");
console.log("Dois");
console.log("Três");

// Importante:
// Não usar ponto e virgula é possível, mas não é recomendado.
// Essa técnica de não suar ponto e virgula é chamada de ASI (Automatic Semicolon Insetion)
// O javascript tenta inserir automanticamente ponto e virgula quando necessãrio.

// (O ponte e virgual marca o fim de uma instrução)