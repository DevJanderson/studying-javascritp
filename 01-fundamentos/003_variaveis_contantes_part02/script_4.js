// Constantes são variáveis que não podem ser alteradas após sua declaração.

const APP_NAME = "MeuAplicativo";
console.log(APP_NAME); // Saída: MeuAplicativo

APP_NAME = "OutroNome"; // Isso causará um erro, pois APP_NAME é uma constante.

// As constantes devem ser inicializadas com um valor 
